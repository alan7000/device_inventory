    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String type = this.jComboBox1.getSelectedItem().toString();
            String priseA = this.jComboBox2.getSelectedItem().toString();
            String priseB = this.jComboBox3.getSelectedItem().toString();
            String nombre = this.jTextFieldNombre.getText();

            if (type.length() == 0) {
                throw new Exception("Veuillez mettre un type ou un \"?\" ");
            }

            if (priseA.length() == 0) {
                throw new Exception("Veuillez mettre un type de prise ou un \"?\" ");
            }

            if (priseB.length() == 0) {
                throw new Exception("Veuillez mettre un type de prise ou un \"?\" ");
            }

            if (nombre.length() == 0) {
                throw new Exception("Veuillez mettre un nombre de cable ou un \"?\" ");
            }

            int nombreInt = Integer.parseInt(nombre);

            cable = new Cable(type, priseA, priseB, nombreInt);
            this.cable.setId(id);
            dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

